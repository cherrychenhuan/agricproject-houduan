package com.agric.myagric.pojo;


public class CropGenerictypes {

  private long cropId;
  private String cropName;
  private String genericId;
  private String genericType;
  private String co2Efftb;
  private String co2Tratb;
  private String co2Amaxtb;


  public long getCropId() {
    return cropId;
  }

  public void setCropId(long cropId) {
    this.cropId = cropId;
  }


  public String getCropName() {
    return cropName;
  }

  public void setCropName(String cropName) {
    this.cropName = cropName;
  }


  public String getGenericId() {
    return genericId;
  }

  public void setGenericId(String genericId) {
    this.genericId = genericId;
  }


  public String getGenericType() {
    return genericType;
  }

  public void setGenericType(String genericType) {
    this.genericType = genericType;
  }


  public String getCo2Efftb() {
    return co2Efftb;
  }

  public void setCo2Efftb(String co2Efftb) {
    this.co2Efftb = co2Efftb;
  }


  public String getCo2Tratb() {
    return co2Tratb;
  }

  public void setCo2Tratb(String co2Tratb) {
    this.co2Tratb = co2Tratb;
  }


  public String getCo2Amaxtb() {
    return co2Amaxtb;
  }

  public void setCo2Amaxtb(String co2Amaxtb) {
    this.co2Amaxtb = co2Amaxtb;
  }

}

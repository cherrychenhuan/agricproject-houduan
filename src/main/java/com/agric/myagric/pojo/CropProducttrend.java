package com.agric.myagric.pojo;



public class CropProducttrend {

  private String provinceCode;
  private String varietyCode;
  private String varietyName;
  private String xyear;
  private String areaSeed;
  private String yieldTotal;


  public String getProvinceCode() {
    return provinceCode;
  }

  public void setProvinceCode(String provinceCode) {
    this.provinceCode = provinceCode;
  }


  public String getVarietyCode() {
    return varietyCode;
  }

  public void setVarietyCode(String varietyCode) {
    this.varietyCode = varietyCode;
  }


  public String getVarietyName() {
    return varietyName;
  }

  public void setVarietyName(String varietyName) {
    this.varietyName = varietyName;
  }


  public String getXyear() {
    return xyear;
  }

  public void setXyear(String xyear) {
    this.xyear = xyear;
  }


  public String getAreaSeed() {
    return areaSeed;
  }

  public void setAreaSeed(String areaSeed) {
    this.areaSeed = areaSeed;
  }


  public String getYieldTotal() {
    return yieldTotal;
  }

  public void setYieldTotal(String yieldTotal) {
    this.yieldTotal = yieldTotal;
  }

}

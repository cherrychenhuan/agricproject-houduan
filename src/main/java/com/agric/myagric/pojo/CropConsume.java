package com.agric.myagric.pojo;



public class CropConsume {

  private String cropid;
  private String cropname;
  private String xyear;
  private String structure;
  private String trenddata;


  public String getCropid() {
    return cropid;
  }

  public void setCropid(String cropid) {
    this.cropid = cropid;
  }


  public String getCropname() {
    return cropname;
  }

  public void setCropname(String cropname) {
    this.cropname = cropname;
  }


  public String getXyear() {
    return xyear;
  }

  public void setXyear(String xyear) {
    this.xyear = xyear;
  }


  public String getStructure() {
    return structure;
  }

  public void setStructure(String structure) {
    this.structure = structure;
  }


  public String getTrenddata() {
    return trenddata;
  }

  public void setTrenddata(String trenddata) {
    this.trenddata = trenddata;
  }

}

package com.agric.myagric.pojo;


public class Recommendationdetails {

  private String id;
  private long recordId;
  private String plantingPlan;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public long getRecordId() {
    return recordId;
  }

  public void setRecordId(long recordId) {
    this.recordId = recordId;
  }


  public String getPlantingPlan() {
    return plantingPlan;
  }

  public void setPlantingPlan(String plantingPlan) {
    this.plantingPlan = plantingPlan;
  }

}
